package DAO_DTO;

public class Tourism_ListDataBean {

	private String park_nm;
	private String park_se;
	private String adres;
	private String gu_nm;

	private String tel;
	public String getPark_nm() {
		return park_nm;
	}
	public void setPark_nm(String park_nm) {
		this.park_nm = park_nm;
	}
	public String getPark_se() {
		return park_se;
	}
	public void setPark_se(String park_se) {
		this.park_se = park_se;
	}
	public String getAdres() {
		return adres;
	}
	public void setAdres(String adres) {
		this.adres = adres;
	}
	public String getGu_nm() {
		return gu_nm;
	}
	public void setGu_nm(String gu_nm) {
		this.gu_nm = gu_nm;
	}
	
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
}
