package DAO_DTO;

public class ScoreDataBean {
	private int schul_num;
	private String t_score;
	private String count;
	private String id;
	private String score;
	
	public int getSchul_num() {
		return schul_num;
	}
	public void setSchul_num(int schul_num) {
		this.schul_num = schul_num;
	}
	public String getT_score() {
		return t_score;
	}
	public void setT_score(String t_score) {
		this.t_score = t_score;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
}
